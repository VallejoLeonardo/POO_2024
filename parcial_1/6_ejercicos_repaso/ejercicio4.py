# y realizar todas las operaciones
# basicas de una calculadora (+,-,*,/)
# y mostrar por pantalla el resultado

a=int(input("Ingrese el segundo número: "))
b=int(input("Ingrese el primer número: "))
suma = (a)+(b)
print(f"La suma es: "+str(suma))
resta = (a)-(b)
print(f"La resta es: "+str(resta))
multi= (a)*(b)
print(f"La multiplicación es: "+str(multi))
division = (a)/(b)
print(f"La division es: "+str(division))